export class Employee {
  constructor(
    id,
    photo, 
    name,
    country,
    role,
    email,
    phone,
    status,
    birthday,
    address,
    city,
    postalCode
  ){
    this.id = id;
    this.photo = photo; 
    this.name = name; 
    this.country = country; 
    this.role = role;
    this.email = email;
    this.phone = phone;
    this.status = status;
    this.birthday = birthday;
    this.address = address;
    this.city = city;
    this.postalCode = postalCode;
  }
}
