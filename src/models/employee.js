export class Employee {
  constructor(
    id,
    photo, 
    name,
    birthday,
    address,
    postalCode,
    city,
    country,
    role,
    email,
    phone,
    status  
  ){
    this.id = id;
    this.photo = photo; 
    this.name = name; 
    this.birthday = birthday;
    this.address = address;
    this.postalCode = postalCode;
    this.city = city;
    this.country = country; 
    this.role = role;
    this.email = email;
    this.phone = phone;
    this.status = status;
  }
}
